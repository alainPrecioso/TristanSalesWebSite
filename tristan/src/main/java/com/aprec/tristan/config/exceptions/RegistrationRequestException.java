package com.aprec.tristan.config.exceptions;

public class RegistrationRequestException extends RegistrationException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 2727513286809008744L;

	public RegistrationRequestException() {
		super();
	}

	public RegistrationRequestException(String message, Throwable cause) {
		super(message, cause);
	}

	public RegistrationRequestException(String s) {
		super(s);
	}

	public RegistrationRequestException(Throwable cause) {
		super(cause);
	}

}
